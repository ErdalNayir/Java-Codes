public class Resolution {
    
    private int hight;
    private int width;

    public Resolution(int hight, int width) {
        this.hight = hight;
        this.width = width;
    }

    /**
     * @return the hight
     */
    public int getHight() {
        return hight;
    }

    /**
     * @param hight the hight to set
     */
    public void setHight(int hight) {
        this.hight = hight;
    }

    /**
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * @param width the width to set
     */
    public void setWidth(int width) {
        this.width = width;
    }
    
    
    
}
